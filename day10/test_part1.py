from part1 import *

def run_test(definition, expected_point, expected_count):
    belt = AsteroidBelt.parse(definition)
    point, count = belt.most_unique()
    assert tuple(point) == tuple(expected_point)
    assert count == expected_count

def test_basic():
    run_test('''
        .#..#
        .....
        #####
        ....#
        ...##
        ''', (3, 4), 8)

